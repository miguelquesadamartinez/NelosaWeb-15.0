              <p align="center" class="Estilo4"><strong>Solucion de Problemas</strong></p>
              <p class="Estilo4">La luz verde no parpadea ni se enciende.</p>
              <ul class="Estilo4">
                <li>Los pernos del reseteador, no est&aacute;n en contacto con los del chip o bien est&aacute;n sucios</li>
                <li>Si el cartucho no es original de Epson, este podr&iacute;a no ser compatible con el reseteador.</li>
                <li>Si el cartucho es original, el chip se podr&iacute;a haber deteriorado o Epson ha cambiado el dise&ntilde;o del chip posterior a la fabricaci&oacute;n del chip.</li>
                <li>Compruebe el estado de las pilas.</li>
              </ul>
              <p class="Estilo4">Si la luz se queda fija, el cartucho ya estaba reseteado</p>
              <p class="Estilo4">Substituci&oacute;n de las bater&iacute;as</p>
              <ol class="Estilo4">
                <li>Destornille la tapa posterior.</li>
                <li>Retire con cuidado la circuito impreso.</li>
              </ol>
              <p class="Estilo4">Substituya las bater&iacute;as y vuelva a montar el reseteador, con mucho cuidado para no doblar los pernos.</p>              </td>
