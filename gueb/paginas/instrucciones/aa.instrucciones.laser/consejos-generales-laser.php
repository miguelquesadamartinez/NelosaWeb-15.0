              <p>En los cartuchos que el tambor ha sido reparado, baja la calidad de impresi&oacute;n: </p>
              <p>La 1&ordf; hoja impresa despu&eacute;s del proceso de recarga, tendr&aacute; la misma calidad de impresi&oacute;n </p>
              <p>que la &uacute;ltima p&aacute;gina que imprimi&oacute; con este cartucho. </p>
              <p>Al reparar el tambor se ajustan los engranajes, pero no mejora la superficie de impresi&oacute;n. </p>
              <p>El cartucho remanufacturado, en su primera recarga, imprimir&aacute; el mismo n&uacute;mero de p&aacute;ginas, que el nuevo. </p>
              <p>Por el desgaste de las partes internas del cartucho (tambor, engranajes, etc ). La longevidad del cartucho y la calidad de impresi&oacute;n, decrecer&aacute;n en las siguientes recargas. </p>
