              <p>Introducir el contenido de una bolsa de 125 gr. de t&oacute;ner, con la ayuda de un embudo de papel </p>
              <p>Cuando haya recargado la mitad, mueva el cartucho, para que el toner se desplace por todo el interior de la c&aacute;mara y contin&uacute;e hasta completar la recarga. </p>
